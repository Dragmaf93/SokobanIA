package jdlv;

public class FirstDirectionJDLV {
	int id;
	String direction;

	public FirstDirectionJDLV() {
	}
	
	public FirstDirectionJDLV(int id, String direction) {
		super();
		this.id = id;
		this.direction = direction;
	}

	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getDirection() {
		return direction;
	}
	public void setDirection(String direction) {
		this.direction = direction;
	}

	
}
